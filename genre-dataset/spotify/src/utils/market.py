# add functionality to check if a market is in available market
def is_market_available(market: str) -> bool:
    return market in available_markets


# build a dictionary with alpha codes and country names using pycountry
def get_alpha2_code_to_name_dict() -> dict:
    codes_and_names = [
        (country.alpha_2, country.name) for country in pycountry.countries
    ]
    return {k: v for k, v in codes_and_names}


# get the country name using pycountry
def alpha2_code_to_name(country_code: str, countries_dict: dict) -> str:
    if country_code in list(countries_dict.keys()):
        return countries_dict[country_code]
    else:
        print(f"Cannot find {country_code} in the existent alpha2 codes.")
        return None


def remove_unavailable_markets(
    markets: list, unavailable_markets: list = ["KR", "XK"]
):
    """Remove unavailable markets like Republique of Korea (KR).
    It never has preview available, so better remove it when we randomize in markets."

    Args:
        markets (list): available market list.
    """
    for unavailable_market in unavailable_markets:
        if unavailable_market in markets:
            markets.remove(unavailable_market)
    return markets
